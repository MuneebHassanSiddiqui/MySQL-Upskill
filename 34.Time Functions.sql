-- 1>CURRENT_TIMESTAMP()
SELECT CURRENT_TIMESTAMP(); -- >OUTPUT=2024-06-02 06:43:26

-- 2>CURTIME()
SELECT CURTIME(); -- >OUTPUT=06:44:26

-- 3>LOCALTIME()
SELECT LOCALTIME(); -- ->OUTPUT= 2024-06-02 06:45:38

-- 4>LOCALTIMESTAMP()
SELECT LOCALTIMESTAMP(); -- ->OUTPUT= 2024-06-02 06:45:38

-- 5>TIME()
SELECT TIME("2024-06-02 06:45:38") AS TIME;

-- 6>HOURS()
SELECT HOUR("2024-06-02 06:45:38") AS HOUR;

-- 7>MINUTES()
SELECT MINUTE("2024-06-02 06:45:38") AS MINUTES;

-- 8>SECOND
SELECT SECOND("2024-06-02 06:45:38") AS SECOND;

-- 9>SECOND
SELECT MICROSECOND("2024-06-02 06:45:38") AS MICROSECOND;

-- 10>TIMEDIFF()
SELECT TIMEDIFF("06:45:38" , "08:50:56") AS TIME_DIFFERENCE;

-- 11>MAKETIME()
SELECT MAKETIME(16,17,8) AS MAKETIME;

-- 12>TIME FORMAT
SELECT TIME_FORMAT("06:45:38" , "%h : %i : %s %p") AS TIME_FORMAT;

-- 13>TIME_TO_SEC()
SELECT TIME_TO_SEC("07:02:57") AS TIME_TO_SECOND; 

-- 13>SEC_TO_TIME()
SELECT SEC_TO_TIME("07:02:57") AS TIME_TO_SECOND; 